package one.part1.basic;

/**
 * HelloWorld 類別
 * 這是學習 Java 的第一個程式範例。
 * 功能：輸出 "Hello, World!"
 */
public class HelloWorld {

    public static void main(String[] args) {
        // 單行註解：這行會在終端機顯示文字
        System.out.println("Hello, World!");
        /*
         * 多行註解：
         * 1. System 是 Java 提供的系統類別
         * 2. out 代表標準輸出（通常是螢幕）
         * 3. println 表示列印一行並換行
         */
    }
}
